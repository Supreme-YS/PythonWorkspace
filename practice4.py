print(6/3) #2

print(2**3) # 2 ^ 3 = 8
print(5%3) #나머지 구하기 2
print(10%3) #나머지 구하기 1
print(5//3) #몫 1
print(10//3) #몫 3

print( 10 > 3) #True 출력
print( 4 >= 7) #False 출력
print( 5 <= 5) #True 출력

print( 3 == 3 ) #앞의 값과 뒤의 값이 똑같은지 확인하는 연산 True 출력
print( 4 == 2 ) #False 출력

print( 1 != 3) #True 출력
print(not(1 != 3)) #False 출력 not은 반대의 개념 opposite

print((3 > 0) and (3 < 5)) #True
print((3 > 0) & (3 < 5)) #True

print((3 > 0) or (3 > 5)) #True 
