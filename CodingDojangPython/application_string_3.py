# formatting


formatting = 'I am %s.' % 'james'
print(formatting)

name = 'Jacob'
formatting2 = 'I am %s.' % name
print(formatting2)


# formatting_integer

formatting3 = 'I am %d years old.' % 20

print(formatting3)

# formatting_float

formatting4 = 'I am %.2fyears old.' % 29.9

print(formatting4)

# formatting_str

formatting5 = 'my job is %20s' % 'data scientist'

print(formatting5)

formatting6 = '%-10s' % 'python'
print(formatting6)
